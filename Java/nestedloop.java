public class nestedloop {
    public static void main(Strings args[]){
        //print a solid rectangle with 4 rows and 5 columns by using stars
        //outer loop(for no of rows)
        for(int i=0;i<4;i++){
            //inner loop
            for(int j=0;j<5;j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
    
}
